#include "libft.h"

int	ft_isnum(const char *str)
{
	if (*str == '-' || *str == '+')
		str++;
	if (!*str)
		return (0);
	while (*str)
	{
		if (!ft_isdigit((unsigned char)*str))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
